class Upsample(nn.Module):
    def __init__(self, in_channels, out_channels, scale_factor=2):
        super(Upsample, self).__init__()
        self.upsample = nn.Sequential(
            Conv(in_channels, out_channels, 1),
            nn.Upsample(scale_factor=scale_factor, mode='bilinear', align_corners=False)
        )

    def forward(self, x):
        return self.upsample(x)


class Downsample(nn.Module):
    def __init__(self, in_channels, out_channels, scale_factor=2):
        super(Downsample, self).__init__()
        # Conv(in, out, k=scale_factor, s=scale_factor, p=0)
        self.downsample = Conv(in_channels, out_channels, scale_factor, scale_factor, 0)

    def forward(self, x):
        return self.downsample(x)


class ASFF3(nn.Module):
    def __init__(self, level=0, channel=[128, 256, 512]):
        """
        ASFF for 3 input feature maps (P2, P3, P4).
        level = 0 -> output alignment P2
        level = 1 -> output alignment P3
        level = 2 -> output alignment P4
        """
        super(ASFF3, self).__init__()
        self.level = level
        self.channel = channel
        self.out_dim = channel[level]
        compress_c = 8
        self.weight_level_1 = Conv(self.out_dim, compress_c, 1, 1)
        self.weight_level_2 = Conv(self.out_dim, compress_c, 1, 1)
        self.weight_level_3 = Conv(self.out_dim, compress_c, 1, 1)

        self.weight_levels = nn.Conv2d(compress_c * 3, 3, kernel_size=1, stride=1, padding=0)
        self.output_conv = Conv(self.out_dim, self.out_dim, 3, 1)

        # 上/下采样模块
        if self.level == 0:
            self.upsample2x = Upsample(channel[1], channel[0], scale_factor=2)
            self.upsample4x = Upsample(channel[2], channel[0], scale_factor=4)
        elif self.level == 1:
            self.downsample2x1 = Downsample(channel[0], channel[1], scale_factor=2)
            self.upsample2x1 = Upsample(channel[2], channel[1], scale_factor=2)
        elif self.level == 2:
            self.downsample4x = Downsample(channel[0], channel[2], scale_factor=4)
            self.downsample2x = Downsample(channel[1], channel[2], scale_factor=2)

    def forward(self, x):
        """
        Forward pass for ASFF3 module.
        Args:
            x: List of 3 input tensors [P2, P3, P4]
        Returns:
            Fused feature tensor
        """
        input1, input2, input3 = x[0], x[1], x[2]
        
        if self.level == 0:
            input2 = self.upsample2x(input2)
            input3 = self.upsample4x(input3)
        elif self.level == 1:
            input1 = self.downsample2x1(input1)
            input3 = self.upsample2x1(input3)
        elif self.level == 2:
            input1 = self.downsample4x(input1)
            input2 = self.downsample2x(input2)

        w1 = self.weight_level_1(input1)
        w2 = self.weight_level_2(input2)
        w3 = self.weight_level_3(input3)

        weights = torch.cat((w1, w2, w3), dim=1)
        weights = self.weight_levels(weights)
        weights = F.softmax(weights, dim=1)

        fused = input1 * weights[:, 0:1] + input2 * weights[:, 1:2] + input3 * weights[:, 2:3]
        out = self.output_conv(fused)
        return out


class TopkRouting(nn.Module):
    """
    differentiable topk routing with scaling
    Args:
        qk_dim: int, feature dimension of query and key
        topk: int, the 'topk'
        qk_scale: int or None, temperature (multiply) of softmax activation
        with_param: bool, wether inorporate learnable params in routing unit
        diff_routing: bool, wether make routing differentiable
        soft_routing: bool, wether make output value multiplied by routing weights
    """
    def __init__(self, qk_dim, topk=4, qk_scale=None, param_routing=False, diff_routing=False):
        super().__init__()
        self.topk = topk
        self.qk_dim = qk_dim
        self.scale = qk_scale or qk_dim ** -0.5
        self.diff_routing = diff_routing
        # TODO: norm layer before/after linear?
        self.emb = nn.Linear(qk_dim, qk_dim) if param_routing else nn.Identity()
        # routing activation
        self.routing_act = nn.Softmax(dim=-1)

    def forward(self, query: Tensor, key: Tensor) -> Tuple[Tensor]:
        """
        Args:
            q, k: (n, p^2, c) tensor
        Return:
            r_weight, topk_index: (n, p^2, topk) tensor
        """
        if not self.diff_routing:
            query, key = query.detach(), key.detach()
        query_hat, key_hat = self.emb(query), self.emb(key)  # per-window pooling -> (n, p^2, c)
        attn_logit = (query_hat * self.scale) @ key_hat.transpose(-2, -1)  # (n, p^2, p^2)
        topk_attn_logit, topk_index = torch.topk(attn_logit, k=self.topk, dim=-1)  # (n, p^2, k), (n, p^2, k)
        r_weight = self.routing_act(topk_attn_logit)  # (n, p^2, k)

        return r_weight, topk_index


class KVGather(nn.Module):
    def __init__(self, mul_weight='none'):
        super().__init__()
        assert mul_weight in ['none', 'soft', 'hard']
        self.mul_weight = mul_weight

    def forward(self, r_idx: Tensor, r_weight: Tensor, kv: Tensor):
        """
        r_idx: (n, p^2, topk) tensor
        r_weight: (n, p^2, topk) tensor
        kv: (n, p^2, w^2, c_kq+c_v)

        Return:
            (n, p^2, topk, w^2, c_kq+c_v) tensor
        """
        # select kv according to routing index
        n, p2, w2, c_kv = kv.size()
        topk = r_idx.size(-1)
        # print(r_idx.size(), r_weight.size())
        # FIXME: gather consumes much memory (topk times redundancy), write cuda kernel?
        topk_kv = torch.gather(kv.view(n, 1, p2, w2, c_kv).expand(-1, p2, -1, -1, -1),
                               # (n, p^2, p^2, w^2, c_kv) without mem cpy
                               dim=2,
                               index=r_idx.view(n, p2, topk, 1, 1).expand(-1, -1, -1, w2, c_kv)
                               # (n, p^2, k, w^2, c_kv)
                               )

        if self.mul_weight == 'soft':
            topk_kv = r_weight.view(n, p2, topk, 1, 1) * topk_kv  # (n, p^2, k, w^2, c_kv)
        elif self.mul_weight == 'hard':
            raise NotImplementedError('differentiable hard routing TBA')
        # else: #'none'
        #     topk_kv = topk_kv # do nothing

        return topk_kv


class QKVLinear(nn.Module):
    def __init__(self, dim, qk_dim, bias=True):
        super().__init__()
        self.dim = dim
        self.qk_dim = qk_dim
        self.qkv = nn.Linear(dim, qk_dim + qk_dim + dim, bias=bias)

    def forward(self, x):
        q, kv = self.qkv(x).split([self.qk_dim, self.qk_dim + self.dim], dim=-1)
        return q, kv
